  @Override
  public AggregatedCaseView searchByView()
  {
    if(this.getSearchType())
    {
      return AggregatedCase.searchByDates(this.getGeoEntity(), this.getStartDate(), this.getEndDate(), this.getAgeGroup());
    }
    else
    {
      PeriodType _periodType = this.getPeriodType().get(0);
            
      return AggregatedCase.searchByGeoEntityAndEpiDate(this.getGeoEntity(), _periodType, this.getPeriod(), this.getPeriodYear(), this.getAgeGroup());
    }
  }
