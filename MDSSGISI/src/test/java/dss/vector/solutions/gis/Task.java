package dss.vector.solutions.gis;

public class Task
{
  private String taskName;

  private int    total;

  private int    work;

  public Task(String taskName, int total)
  {
    this.taskName = taskName;
    this.total = total;
    this.work = 0;
  }

  public String getTaskName()
  {
    return taskName;
  }

  public int getTotal()
  {
    return total;
  }

  public int getWork()
  {
    return work;
  }

  public void addWork(int work)
  {
    this.work += work;
  }

}
